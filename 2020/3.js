// return array of coordinates (y, x) that exist on the board
function getCoords(board, yStep, xStep) {
  const yMax = board.length;
  const xMax = board[0].length;
  const coords = [];
  let y = yStep;
  let x = xStep;
  while (y < yMax && x < xMax) {
    coords.push([y, x]);
    y += yStep;
    x += xStep;
  }
  return coords;
}

